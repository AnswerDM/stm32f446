/*            Cortex-M4 Processor Exceptions Handlers                         */
/******************************************************************************/
extern volatile uint32_t sysTickUptime;
extern int count_test;
extern uint16_t receive_buff[256];
uint8_t count = 0;


void SysTick_Handler(void)
{
	 sysTickUptime++;
}


void TIM3_IRQHandler(void)
	
{
	if(TIM_GetITStatus(TIM3,TIM_IT_Update)==SET) //溢出中断
	{
		count_test ++;
		if(count_test%2 ==0)
		{
			GPIOB->BSRRL = GPIO_Pin_8;
		}
		else
		{
			GPIOB->BSRRH = GPIO_Pin_8;
		}
	}
	TIM_ClearITPendingBit(TIM3,TIM_IT_Update);  //清除中断标志位
}


void EXTI15_10_IRQHandler(void)
{
	if(EXTI_GetITStatus(EXTI_Line13)==SET)
	{
			LED_ON;
	}	
	 
	 EXTI_ClearITPendingBit(EXTI_Line13);  //清除LINE3上的中断标志位  
}


void USART2_IRQHandler(void)
{
	uint16_t ch;
	
	
	if(USART_GetITStatus(USART2,USART_IT_RXNE)!=RESET) //判断是否为接收中断
	{
		ch = USART_ReceiveData(USART2);
		receive_buff[count] = ch;
		count++;
//		if(count==3){count=0;}
		USART_ClearITPendingBit(USART2,USART_IT_RXNE);
	}
}
	






